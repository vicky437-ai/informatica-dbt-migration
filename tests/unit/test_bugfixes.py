class TestC4NestedNvl2:
    """C4: _convert_nvl2 must handle nested parens correctly."""

    def test_simple_nvl2(self):
        result = _convert_nvl2("NVL2(X, 'yes', 'no')")
        assert result == "IFF(X IS NOT NULL, 'yes', 'no')"

    def test_nested_nvl2_trim(self):
        """NVL2(TRIM(X), a, b) must balance nested parens."""
        result = _convert_nvl2("NVL2(TRIM(X), 'a', 'b')")
        assert result == "IFF(TRIM(X) IS NOT NULL, 'a', 'b')"

    def test_nested_nvl2_in_args(self):
        """NVL2 with nested function calls in all arguments."""
        result = _convert_nvl2("NVL2(TRIM(X), UPPER(Y), LOWER(Z))")
        assert result == "IFF(TRIM(X) IS NOT NULL, UPPER(Y), LOWER(Z))"

    def test_case_insensitive(self):
        result = _convert_nvl2("nvl2(a, b, c)")
        assert result == "IFF(a IS NOT NULL, b, c)"


