class TestC3NestedIsnull:
    """C3: _convert_isnull_boolean must handle nested parens correctly."""

    def test_simple_isnull(self):
        result = _convert_isnull_boolean("ISNULL(X)")
        assert result == "(X IS NULL)"

    def test_nested_isnull_trim(self):
        """ISNULL(TRIM(X)) must not break the paren matching."""
        result = _convert_isnull_boolean("ISNULL(TRIM(X))")
        assert result == "(TRIM(X) IS NULL)"

    def test_deeply_nested_isnull(self):
        """ISNULL(UPPER(TRIM(X))) must balance correctly."""
        result = _convert_isnull_boolean("ISNULL(UPPER(TRIM(X)))")
        assert result == "(UPPER(TRIM(X)) IS NULL)"

    def test_two_arg_isnull_left_alone(self):
        """Two-arg form ISNULL(x, default) should NOT be converted to IS NULL."""
        result = _convert_isnull_boolean("ISNULL(X, 0)")
        # Two-arg form stays as-is for the later ISNULL→IFNULL mapping
        assert "IS NULL" not in result
        assert "ISNULL" in result

    def test_case_insensitive(self):
        result = _convert_isnull_boolean("isnull(col_a)")
        assert result == "(col_a IS NULL)"


