class TestH5SubChainConnectors:
    """H5: When a chain is split into sub-groups, connectors between
    sub-groups must be preserved via connector_scope."""

    def test_collect_chain_objects_with_scope(self):
        """Connectors between sub-groups should be included when
        connector_scope covers the full parent chain."""
        from informatica_to_dbt.chunker.context_preserving import _collect_chain_objects

        mapping = Mapping(
            name="m_scope_test",
            transformations=[
                Transformation(name="SQ_A", type="Source Qualifier", fields=[]),
                Transformation(name="EXP_B", type="Expression", fields=[]),
                Transformation(name="TGT_C", type="Target Definition", fields=[]),
            ],
            instances=[
                Instance(name="SQ_A", transformation_name="SQ_A",
                         transformation_type="Source Qualifier"),
                Instance(name="EXP_B", transformation_name="EXP_B",
                         transformation_type="Expression"),
                Instance(name="TGT_C", transformation_name="TGT_C",
                         transformation_type="Target Definition", instance_type="TARGET"),
            ],
            connectors=[
                # SQ_A → EXP_B
                Connector(from_field="F1", from_instance="SQ_A",
                          from_instance_type="Source Qualifier",
                          to_field="F1", to_instance="EXP_B",
                          to_instance_type="Expression"),
                # EXP_B → TGT_C
                Connector(from_field="F1", from_instance="EXP_B",
                          from_instance_type="Expression",
                          to_field="F1", to_instance="TGT_C",
                          to_instance_type="Target Definition"),
            ],
        )

        full_chain = ["SQ_A", "EXP_B", "TGT_C"]

        # Without connector_scope: only SQ_A chunk — no connectors because
        # EXP_B is not in the chunk
        transforms, instances, connectors = _collect_chain_objects(
            mapping, ["SQ_A"], connector_scope=None,
        )
        assert len(connectors) == 0, "Without scope, cross-chunk connectors must be excluded"

        # With connector_scope = full chain: SQ_A → EXP_B connector should
        # be included because both ends are in the full chain scope
        transforms, instances, connectors = _collect_chain_objects(
            mapping, ["SQ_A"], connector_scope=full_chain,
        )
        assert len(connectors) == 1, "With scope, SQ_A→EXP_B connector must be included"
        assert connectors[0].from_instance == "SQ_A"
        assert connectors[0].to_instance == "EXP_B"

    def test_collect_chain_objects_both_ends_must_be_in_scope(self):
        """Connectors where one end is outside the scope must be excluded."""
        from informatica_to_dbt.chunker.context_preserving import _collect_chain_objects

        mapping = Mapping(
            name="m_scope_edge",
            transformations=[
                Transformation(name="A", type="Expression", fields=[]),
                Transformation(name="B", type="Expression", fields=[]),
                Transformation(name="OUTSIDE", type="Expression", fields=[]),
            ],
            instances=[
                Instance(name="A", transformation_name="A", transformation_type="Expression"),
                Instance(name="B", transformation_name="B", transformation_type="Expression"),
                Instance(name="OUTSIDE", transformation_name="OUTSIDE",
                         transformation_type="Expression"),
            ],
            connectors=[
                Connector(from_field="F", from_instance="A", from_instance_type="Expression",
                          to_field="F", to_instance="B", to_instance_type="Expression"),
                # This connector has one end outside the scope
                Connector(from_field="F", from_instance="B", from_instance_type="Expression",
                          to_field="F", to_instance="OUTSIDE", to_instance_type="Expression"),
            ],
        )

        # Scope is only [A, B] — OUTSIDE is not in scope
        transforms, instances, connectors = _collect_chain_objects(
            mapping, ["A"], connector_scope=["A", "B"],
        )
        assert len(connectors) == 1
        assert connectors[0].to_instance == "B"


