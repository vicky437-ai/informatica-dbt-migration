class TestC1ProjectYmlMerge:
    """C1: _write_project_yml must preserve prior mapping configs."""

    def test_existing_mappings_preserved(self, tmp_path):
        """When dbt_project.yml already has mapping A and we write mapping B,
        both A and B must appear in the final file."""
        from informatica_to_dbt.merger.project_merger import ProjectMerger, MergeResult
        from informatica_to_dbt.orchestrator import MappingResult
        from informatica_to_dbt.config import Config

        project_dir = tmp_path / "dbt_project"
        project_dir.mkdir()

        # Pre-populate an existing dbt_project.yml with mapping_a
        existing_yml = textwrap.dedent("""\
            # -- Generated by infa2dbt
            name: 'test_project'
            version: '1.0.0'
            profile: 'informatica_to_dbt_migration'
            require-dbt-version: ['>=1.7.0', '<2.0.0']
            model-paths: ['models']
            analysis-paths: ['analyses']
            test-paths: ['tests']
            seed-paths: ['seeds']
            macro-paths: ['macros']
            snapshot-paths: ['snapshots']
            clean-targets:
              - 'target'
              - 'dbt_packages'
            models:
              test_project:
                mapping_a:
                  +tags: ['mapping_a']
                  staging:
                    +materialized: view
        """)
        (project_dir / "dbt_project.yml").write_text(existing_yml, encoding="utf-8")

        # Create a MappingResult for mapping_b with a staging file
        mr_b = MappingResult(
            mapping_name="mapping_b",
            workflow_name="wf_test",
            files=[
                GeneratedFile("models/mapping_b/staging/stg_b.sql", "SELECT 1"),
            ],
        )

        config = Config()
        config.project_dir = str(project_dir)
        config.dbt_project_name = "test_project"
        config.merge_mode = "merge"

        merger = ProjectMerger(config)
        result = merger.merge([mr_b])

        # Re-read the dbt_project.yml
        final = yaml.safe_load(
            (project_dir / "dbt_project.yml").read_text(encoding="utf-8")
        )
        project_models = final["models"]["test_project"]

        # Both mapping_a (existing) and mapping_b (new) must be present
        assert "mapping_a" in project_models, "Existing mapping_a was overwritten"
        assert "mapping_b" in project_models, "New mapping_b not written"


