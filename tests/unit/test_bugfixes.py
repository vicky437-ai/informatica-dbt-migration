        from informatica_to_dbt.metrics import RepositoryMetrics, MappingMetrics

        # Build an existing report with one mapping
        existing_report = {
            "title": "Test",
            "generated_at": "2024-01-01 00:00:00",
            "summary": {
                "mappings": [
                    {
                        "mapping_name": "m_old",
                        "status": "success",
                        "total_seconds": 1.0,
                        "llm_calls": 1,
                        "llm_total_seconds": 0.5,
                        "complexity_score": 30,
                        "strategy": "DIRECT",
                        "quality_score": 90.0,
                        "total_errors": 0,
                        "total_warnings": 0,
                        "files_generated": 3,
                        "self_heal_rounds": 0,
                        "cache_hit": False,
                    }
                ],
                "total_seconds": 1.0,
                "total_mappings": 1,
                "successful": 1,
                "failed": 0,
                "success_rate": 100.0,
            },
            "items": [
                {
                    "level": "info",
                    "mapping_name": "m_old",
                    "code": "I001",
                    "message": "Conversion succeeded",
                    "detail": "",
                }
            ],
        }

        json_path = tmp_path / "ewi_report.json"
        json_path.write_text(json.dumps(existing_report), encoding="utf-8")

        # Now write a new report for mapping m_new
        m_metrics = MappingMetrics(mapping_name="m_new")
        m_metrics.status = "success"
        m_metrics.total_seconds = 2.0
        repo_metrics = RepositoryMetrics(
            mapping_metrics=[m_metrics], total_seconds=2.0,
        )

        new_report = EWIReport(
            repository_metrics=repo_metrics,
            items=[
                EWIItem(
                    level="info",
                    mapping_name="m_new",
                    code="I001",
                    message="Conversion succeeded",
                ),
            ],
        )

        generator = EWIReportGenerator()
        generator.write_json(new_report, str(json_path))

        result = json.loads(json_path.read_text(encoding="utf-8"))
        mapping_names = [
            m["mapping_name"]
            for m in result["summary"]["mappings"]
        ]
        assert "m_old" in mapping_names, "Existing m_old was lost"
        assert "m_new" in mapping_names, "New m_new not written"

        # Items from both should be present
        item_mappings = [i["mapping_name"] for i in result["items"]]
        assert "m_old" in item_mappings
        assert "m_new" in item_mappings


