class TestM2ParameterizedQuery:
    """M2: schema_discovery.from_snowflake must use parameterized queries."""

    def _import_schema_discovery(self):
        """Import schema_discovery, skipping if lxml is unavailable."""
        try:
            from informatica_to_dbt.discovery.schema_discovery import (
                SchemaDiscovery,
                _sanitize_identifier,
            )
            return SchemaDiscovery, _sanitize_identifier
        except ImportError:
            pytest.skip("lxml unavailable — discovery package cannot be imported")

    def test_query_uses_bind_parameter(self):
        """The SQL query built for INFORMATION_SCHEMA.COLUMNS must use '?'
        for the WHERE clause value, not f-string interpolation."""
        import inspect
        SchemaDiscovery, _ = self._import_schema_discovery()

        source = inspect.getsource(SchemaDiscovery.from_snowflake)

        # Must contain bind parameter marker
        assert "WHERE TABLE_SCHEMA = ?" in source or "WHERE TABLE_SCHEMA = ?" in source, \
            "Query must use bind parameter for TABLE_SCHEMA"

        # Must NOT contain f-string interpolation for WHERE clause
        assert "'{safe_schema}'" not in source, \
            "Query must not use f-string interpolation for WHERE value"

    def test_cursor_execute_called_with_params(self):
        """Verify that cursor.execute is called with a params tuple."""
        SchemaDiscovery, _ = self._import_schema_discovery()

        mock_session = MagicMock()
        mock_cursor = MagicMock()
        mock_session.connection.cursor.return_value = mock_cursor
        mock_cursor.fetchall.return_value = []
        mock_cursor.description = []

        SchemaDiscovery.from_snowflake(mock_session, "MY_DB", "MY_SCHEMA")

        # Verify cursor.execute was called with a tuple param
        mock_cursor.execute.assert_called_once()
        call_args = mock_cursor.execute.call_args
        sql_arg = call_args[0][0]
        params_arg = call_args[0][1]

        assert "?" in sql_arg, "SQL must contain bind placeholder"
        assert params_arg == ("MY_SCHEMA",), \
            f"Bind params must be ('MY_SCHEMA',), got {params_arg}"

    def test_sanitize_identifier_strips_injection(self):
        """_sanitize_identifier must strip SQL injection characters."""
        _, _sanitize_identifier = self._import_schema_discovery()

        # Normal identifier passes through
        assert _sanitize_identifier("MY_SCHEMA") == "MY_SCHEMA"
        assert _sanitize_identifier("DB$1") == "DB$1"

        # Injection attempts are stripped
        assert _sanitize_identifier("MY'; DROP TABLE--") == "MYDROPTABLE"

        # Empty result raises ValueError
        with pytest.raises(ValueError):
            _sanitize_identifier("!@#%^&*()")
