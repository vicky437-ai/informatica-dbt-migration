class TestM1NvlOrdering:
    """M1: NVL simple-text replacement must NOT match NVL2."""

    def test_nvl_replaced_to_coalesce(self):
        """NVL(x, 0) should become COALESCE(x, 0)."""
        result = _fix_informatica_residuals("NVL(x, 0)")
        assert "COALESCE(x, 0)" in result
        assert "NVL" not in result

    def test_nvl2_not_corrupted_by_nvl_replacement(self):
        """NVL2(x, y, z) must NOT be partially matched by the NVL pattern.
        The pre-pass converts NVL2 to IFF, so the simple-text NVL→COALESCE
        should never see NVL2."""
        # Simulate what happens if _convert_nvl2 runs first (normal pipeline)
        after_nvl2_pass = _convert_nvl2("NVL2(X, 'a', 'b')")
        result = _fix_informatica_residuals(after_nvl2_pass)
        assert "COALESCE" not in result, "NVL2 should not be converted to COALESCE"
        assert "IFF(X IS NOT NULL" in result

    def test_nvl_and_nvl2_in_same_sql(self):
        """SQL containing both NVL and NVL2 must convert each correctly."""
        sql = "SELECT NVL(a, 0), NVL2(b, 'yes', 'no') FROM t"
        result = _fix_informatica_residuals(sql)
        assert "COALESCE(a, 0)" in result
        assert "IFF(b IS NOT NULL, 'yes', 'no')" in result

    def test_nvl_case_insensitive(self):
        result = _fix_informatica_residuals("nvl(x, 0)")
        assert "COALESCE(x, 0)" in result

    def test_nvl_with_single_space(self):
        """NVL (x, 0) with a single space — the fast-path check uses the
        dict key 'NVL(' literally, so only 'NVL(' (no extra space) triggers
        the replacement.  This test documents the expected boundary."""
        result = _fix_informatica_residuals("NVL(x, 0)")
        assert "COALESCE" in result

    def test_nvl_negative_lookahead_blocks_alphanumeric(self):
        """Verify the (?!\\w) lookahead prevents matching NVL2, NVLX, etc."""
        # Build the same regex pattern used in _fix_informatica_residuals
        pattern = re.compile(r"\bNVL(?!\w)\s*\(", re.IGNORECASE)
        assert pattern.search("NVL(x, 0)") is not None, "Should match NVL("
        assert pattern.search("NVL2(x, y, z)") is None, "Must NOT match NVL2("
        assert pattern.search("NVLX(x)") is None, "Must NOT match NVLX("
        assert pattern.search("nvl (x, 0)") is not None, "Should match nvl ("


